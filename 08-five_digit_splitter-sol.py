number = input("Please give a 5 digit number: ")

# the \n escape sequence denotes a "newline" character that
# causes subsequent characters to be printed on a new line
result = number[0] + "\n" + number[1] + "\n" + number[2] + "\n" + number[3] + "\n" + number[4] + "\n"

print("The result is:\n" +result)